// ── Helpers ────────────────────────────────────────────────────────────────────

export const getFeaturedSermon = () =>
  sermons.find(s => s.featured) ?? sermons[0]

export const getRecentSermons = (count = 6) =>
  sermons.filter(s => s.category === 'message' || s.category === 'conference').slice(0, count)

export const getByCategory = (category: SermonCategory, count?: number) => {
  const filtered = sermons.filter(s => s.category === category)
  return count ? filtered.slice(0, count) : filtered
}

export const getMeditations = (count?: number) => {
  const med = sermons.filter(s => s.category === 'meditation')
  return count ? med.slice(0, count) : med
}

export const getMessages = (count?: number) => {
  const msg = sermons.filter(s => s.category === 'message')
  return count ? msg.slice(0, count) : msg
}

export const getConferences = (count?: number) => {
  const conf = sermons.filter(s => s.category === 'conference')
  return count ? conf.slice(0, count) : conf
}

export const getEnseignements = (count?: number) => {
  const ens = sermons.filter(s => s.category === 'enseignement')
  return count ? ens.slice(0, count) : ens
}
